package newdata;

public class Settingobj {

	public String duration;
	public String barlength;
	public String avg1;
	public String avg2;
	public String adjustment;
	public String getDuration() {
		return duration;
	}
	public void setDuration(String duration) {
		this.duration = duration;
	}
	public String getBarlength() {
		return barlength;
	}
	public void setBarlength(String barlength) {
		this.barlength = barlength;
	}
	public String getAvg1() {
		return avg1;
	}
	public void setAvg1(String avg1) {
		this.avg1 = avg1;
	}
	public String getAvg2() {
		return avg2;
	}
	public void setAvg2(String avg2) {
		this.avg2 = avg2;
	}
	public String getAdjustment() {
		return adjustment;
	}
	public void setAdjustment(String adjustment) {
		this.adjustment = adjustment;
	}
	
}
